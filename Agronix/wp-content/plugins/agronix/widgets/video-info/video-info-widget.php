    protected function render() {
        $settings = $this->get_settings_for_display();

        $this->add_inline_editing_attributes( 'title', 'basic' );
        $this->add_render_attribute( 'title', 'class', '' );

        $this->add_inline_editing_attributes( 'description', 'intermediate' );
        $this->add_render_attribute( 'description', 'class', 'bdevs-infobox-text' );

        ?>

        <?php if ($settings['design_style'] === 'style_2'):

        if (!empty($settings['image']['id'])) {
            $image = wp_get_attachment_image_url($settings['image']['id'], 'full');
        }

        if (!empty($settings['image2']['id'])) {
            $image2 = wp_get_attachment_image_url($settings['image2']['id'], 'full');
        }

        ?>

        <div class="video-area" data-background="<?php echo esc_html($image); ?>">
            <div class="play-btn">
               <img src="<?php echo esc_html($image2); ?>" alt="img">
               <a href="<?php echo esc_url( $settings['video_url'] ); ?>" class="play-text play-text-2 popup-video"><?php echo bdevs_element_kses_intermediate($settings['title']); ?></a>
            </div>
        </div>

        <?php else:

            if (!empty($settings['image']['id'])) {
                $image = wp_get_attachment_image_url($settings['image']['id'], 'full');
            }

